def is_vow(inp):
   return [chr(i) if chr(i) in 'aiueo' else i for i in inp]


def is_vow(s):
   vowels = {97: 'a', 111: 'o', 117: 'u', 101: 'e', 105: 'i'}
   return [vowels.get(a, a) for a in s]


def is_vow(inp):
   return [chr(v) if v in map(ord, "aeiou") else v for v in inp]


